def praise_line(github_username: str, commit_count: int) -> str:
    """커밋 수 구간별 칭찬 멘트 반환."""
    n = commit_count

    if n >= 10:
        pool = _ULTRA.get(github_username, _ULTRA_GENERIC)
    elif n >= 5:
        pool = _HYPE.get(github_username, _HYPE_GENERIC)
    else:
        pool = _BASE.get(github_username, _BASE_GENERIC)

    return random.choice(pool).replace("{n}", str(n))


def company_of(github_username: str) -> str:
    companies = {"Byesol": "현대오토에버", "gahyunkim": "현대모비스"}
    return companies.get(github_username, "")


def done_line(did: str, gh_name: str, cnt: int) -> str:
    """달성자 표시 줄 + 칭찬 멘트."""
    header = f"✅ <@{did}> `{gh_name}` — {cnt}개 🔥"
    praise = praise_line(gh_name, cnt)
    return f"{header}\n> {praise}"


def short_line(did: str, gh_name: str, cnt: int, min_commits: int) -> str:
    """미달자 표시 줄."""
    remaining = min_commits - cnt
    return f"❌ <@{did}> `{gh_name}` — {cnt}개 (앞으로 **{remaining}개** 더요!!)"
